from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
SOURCE_PATH = REPO_ROOT / "data" / "source_documents" / "cn-nl-main-treaty.json"
DEFAULT_OUTPUT_PATH = REPO_ROOT / "data" / "treaties" / "cn-nl.v3.generated.json"
SUPPORTED_INCOME_TYPES = {"dividends", "interest", "royalties"}
SUPPORTED_TEXT_QUALITIES = {"clean", "warning", "ocr-risk"}
SUPPORTED_NORMALIZATION_STATUSES = {"verbatim", "normalized"}


class SourceValidationError(ValueError):
    pass


def load_source_document(source_path: Path) -> dict:
    with source_path.open("r", encoding="utf-8") as file:
        return json.load(file)


def build_dataset(source_payload: dict) -> dict:
    document = source_payload["document"]
    articles: list[dict] = []

    for parsed_article in source_payload["parsed_articles"]:
        article = {
            "article_number": parsed_article["article_number"],
            "article_title": parsed_article["article_title"],
            "article_label": parsed_article["article_label"],
            "income_type": parsed_article["income_type"],
            "summary": parsed_article["summary"],
            "notes": parsed_article["article_notes"],
            "paragraphs": [],
        }

        for paragraph in parsed_article["paragraphs"]:
            source_segments = normalize_source_segments(paragraph)
            provenance_summary = build_paragraph_provenance_summary(
                source_segments=source_segments,
                rules=paragraph["extracted_rules"],
            )
            article["paragraphs"].append(
                {
                    "paragraph_id": paragraph["paragraph_id"],
                    "paragraph_label": paragraph["paragraph_label"],
                    "source_reference": paragraph["source_reference"],
                    "source_language": paragraph["source_language"],
                    "source_excerpt": build_source_excerpt(source_segments),
                    "source_segments": source_segments,
                    "provenance_summary": provenance_summary,
                    "rules": paragraph["extracted_rules"],
                }
            )

        articles.append(article)

    return {
        "treaty": build_treaty_metadata(document),
        "articles": articles,
    }


def build_treaty_metadata(document: dict) -> dict:
    jurisdictions = document["jurisdictions"]
    treaty_id = "-".join(jurisdiction.lower() for jurisdiction in jurisdictions)
    metadata = {
        "treaty_id": treaty_id,
        "jurisdictions": jurisdictions,
        "title": document["title"],
        "version": "v3-generated",
        "source_type": "import_stub_from_source_documents",
        "source_documents": [
            {
                "document_id": document["document_id"],
                "title": document["title"],
                "document_type": document["document_type"],
            }
        ],
        "notes": [
            "Generated by the pair-agnostic source-document import stub.",
            "Uses structured source segments as a placeholder for later parser output.",
        ],
    }
    if "source_trace" in document:
        metadata["source_trace"] = document["source_trace"]
    if "mli_context" in document:
        metadata["mli_context"] = document["mli_context"]
    return metadata


def validate_source_payload(source_payload: dict) -> None:
    seen_rule_ids: set[str] = set()

    for parsed_article in source_payload["parsed_articles"]:
        income_type = parsed_article["income_type"]
        if income_type not in SUPPORTED_INCOME_TYPES:
            raise SourceValidationError(f"Found unsupported income_type: {income_type}")

        for paragraph in parsed_article["paragraphs"]:
            if not paragraph["source_reference"].strip():
                raise SourceValidationError(
                    f"Segment {paragraph['paragraph_id']} is missing source_reference."
                )

            source_segments = normalize_source_segments(paragraph)
            seen_segment_ids: set[str] = set()
            for segment in source_segments:
                segment_id = segment["segment_id"]
                if segment_id in seen_segment_ids:
                    raise SourceValidationError(
                        f"Found duplicate source segment: {segment_id}"
                    )
                seen_segment_ids.add(segment_id)
                if segment["text_quality"] not in SUPPORTED_TEXT_QUALITIES:
                    raise SourceValidationError(
                        f"Found invalid text_quality for {segment_id}: {segment['text_quality']}"
                    )
                if segment["normalization_status"] not in SUPPORTED_NORMALIZATION_STATUSES:
                    raise SourceValidationError(
                        "Found invalid normalization_status for "
                        f"{segment_id}: {segment['normalization_status']}"
                    )
            segment_ids = {segment["segment_id"] for segment in source_segments}

            for rule in paragraph["extracted_rules"]:
                extraction_confidence = rule["extraction_confidence"]
                if not 0 <= extraction_confidence <= 1:
                    raise SourceValidationError(
                        f"Found invalid extraction_confidence for {rule['rule_id']}: {extraction_confidence}"
                    )
                rule_id = rule["rule_id"]
                if rule_id in seen_rule_ids:
                    raise SourceValidationError(f"Found duplicate rule_id: {rule_id}")
                seen_rule_ids.add(rule_id)
                for segment_id in rule.get("derived_from_segments", []):
                    if segment_id not in segment_ids:
                        raise SourceValidationError(
                            f"Rule {rule_id} references unknown source segment: {segment_id}"
                        )

            primary_candidates = [
                rule["rule_id"]
                for rule in paragraph["extracted_rules"]
                if rule.get("is_primary_candidate") is True
            ]
            if len(primary_candidates) > 1:
                raise SourceValidationError(
                    "Found multiple primary candidates in one paragraph: "
                    + ", ".join(primary_candidates)
                )


def normalize_source_segments(paragraph: dict) -> list[dict]:
    if "source_segments" in paragraph:
        segments = sorted(paragraph["source_segments"], key=lambda segment: segment["segment_order"])
        segments = [
            {
                "segment_id": segment["segment_id"],
                "segment_order": segment["segment_order"],
                "raw_line_number": segment.get("raw_line_number"),
                "page_hint": segment.get("page_hint"),
                "source_kind": segment["source_kind"],
                "text_quality": segment.get("text_quality", "clean"),
                "normalization_status": segment.get("normalization_status", "verbatim"),
                "text": segment["text"],
            }
            for segment in segments
        ]
        return segments

    return [
        {
            "segment_id": f"{paragraph['paragraph_id']}-s1",
            "segment_order": 1,
            "page_hint": None,
            "source_kind": "article_paragraph",
            "text_quality": "clean",
            "normalization_status": "verbatim",
            "text": paragraph["source_excerpt"],
        }
    ]


def build_source_excerpt(source_segments: list[dict]) -> str:
    return " ".join(segment["text"].strip() for segment in source_segments if segment["text"].strip())


def select_primary_candidate(rules: list[dict]) -> dict | None:
    for rule in rules:
        if rule.get("is_primary_candidate") is True:
            return rule

    if not rules:
        return None

    return min(rules, key=lambda rule: rule.get("candidate_rank", 999))


def build_paragraph_provenance_summary(source_segments: list[dict], rules: list[dict]) -> dict:
    primary_rule = select_primary_candidate(rules)
    if primary_rule is None:
        return {
            "primary_rule_id": None,
            "paragraph_confidence": None,
            "segment_count": len(source_segments),
            "covered_segment_count": 0,
            "segment_coverage": 0.0,
        }

    segment_count = len(source_segments)
    covered_segment_count = len(primary_rule.get("derived_from_segments", []))
    segment_coverage = 0.0
    if segment_count > 0:
        segment_coverage = round(covered_segment_count / segment_count, 4)

    return {
        "primary_rule_id": primary_rule["rule_id"],
        "paragraph_confidence": primary_rule["extraction_confidence"],
        "segment_count": segment_count,
        "covered_segment_count": covered_segment_count,
        "segment_coverage": segment_coverage,
    }


def write_dataset(payload: dict, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build a treaty dataset from a parser-like source document payload."
    )
    parser.add_argument(
        "--source",
        type=Path,
        default=SOURCE_PATH,
        help="Source document JSON path.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT_PATH,
        help="Output path for the generated dataset JSON.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    source_payload = load_source_document(args.source)
    try:
        validate_source_payload(source_payload)
    except SourceValidationError as error:
        print(str(error), file=sys.stderr)
        return 1
    dataset = build_dataset(source_payload)
    write_dataset(dataset, args.output)
    print(f"Wrote dataset to {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
